<?php

$x = 0;

switch($x) {

    case 0:
        echo "x é igual a 0 <br>";
        break;
    case 1:
        echo "x é igual a 1 <br>";
        break;
    case 2:
         echo "x é igual a 2 <br>";
         break;
    case 3:
        echo "x é igual a 3 <br>";
        break;
    default:
        echo "x não é igual a nenhum dos valores mencionados <br>";
}


